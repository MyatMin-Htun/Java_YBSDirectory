    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        id=getAutoID("BusDriverDetailID","busdriverdetail","BDD");
        boolean cond=true;
        try{
            if(CboDriverName.getSelectedIndex()==0){
                Errorlblname.setForeground(new java.awt.Color(255, 0, 0));
    		Errorlblname.setText("*Please Choose Driver Name.");
                cond=false;
            }  
            else if(CboBusLineNo.getSelectedIndex()==0){
                ErrorlblBusLine.setForeground(new java.awt.Color(255, 0, 0));
                ErrorlblBusLine.setText("*Please Choose Bus Line.");
                cond=false;
            }
            else if(txtLisence.getText().trim().isEmpty()){
                
                ErrorlblLicense.setForeground(new java.awt.Color(255, 0, 0));
                ErrorlblLicense.setText("*Please Enter License.");
                txtLisence.requestFocus();
                cond=false;
            }
                
            else if(!(txtLisence.getText().trim().isEmpty())){
                String l=txtLisence.getText().toString().trim();
                StringTokenizer st=new StringTokenizer(l,"/");                                
                
                if(st.countTokens()==1){
                    throw new liscenseException();
                }
                //System.out.print(count);
                else {
                    
                    Pattern pattern=Pattern.compile("[a-zA-Z0-9/]+$");
                    Matcher matcher=pattern.matcher(txtLisence.getText().trim());
                        if(!matcher.find()) {
                            cond=false;
                           throw new SpecialCharacterException();
                        }
                }
            }
            else{
                cond=true;
            }
            if(cond){
                String Did=null;
                ste=con.createStatement();
                String query="SELECT BusDriverID FROM busdriver WHERE BusDriverName ='" + CboDriverName.getSelectedItem().toString()  + "'";
                rs=ste.executeQuery(query); 
                if(rs.next()){
                     Did=rs.getString(1);
                }
                query= "insert into busdriverdetail(BusDriverDetailID,BusLineNo,BusDriverID,Plate,Date) values ('"+id+"','"+CboBusLineNo.getSelectedItem().toString()+"','"+Did+"','"+txtLisence.getText().toString()+"','"+dateformat.format(jDateChooser1.getDate())+"')";
                int result=ste.executeUpdate(query);
                if(result==1){
                    clearData();
                    JOptionPane.showMessageDialog(this,"This User record is save successfully.");
                    
                  //update detail in update detail table    				
    				addUpdateDetail("added bus info for license plate : " + txtLisence.getText().toString().trim());
                }
            }
            
        }catch(liscenseException e){
            ErrorlblLicense.setForeground(new java.awt.Color(255, 0, 0));
            txtLisence.requestFocus();
            txtLisence.setText("");
            ErrorlblLicense.setText(e.getMessage());
            }
        catch(SpecialCharacterException e){
            ErrorlblLicense.setForeground(new java.awt.Color(255, 0, 0));
            txtLisence.requestFocus();
            txtLisence.setText("");
            ErrorlblLicense.setText(e.getMessage());
           }
        catch(Exception e){
            System.out.print(e);
            System.out.println(id);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

