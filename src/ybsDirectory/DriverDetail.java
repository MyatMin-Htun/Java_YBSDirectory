    private void addCboDriverName(JComboBox cboBox) {
        boolean cond=true;
    	cboBox.removeAllItems();
    	cboBox.addItem("------SELECT------");
    	try {
    		ste=con.createStatement();
    		String query="SELECT DISTINCT BusDriverName FROM busdriver ORDER BY BusDriverName";
    		rs=ste.executeQuery(query);
    		
    		while(rs.next()) {
                    if(rs.getString(1).equals("null")){
                        cond=false;
                    }
                    else{
                        cond=true;
                    }
                    if(cond){
                        cboBox.addItem(rs.getString(1));
                    }
    		}
    	}catch(Exception e) {
    		System.out.println(e);
    	}
    }
    
